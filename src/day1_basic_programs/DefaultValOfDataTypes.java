package day1_basic_programs;

public class DefaultValOfDataTypes {
    static int a;
    static short b;
    static long c;
    static double d;
    static float e;
    static String f;
    static char g;
    static boolean h;

    public static void main(String[] args) {
        System.out.println("Printing Default values of Primitive DataTypes");
        System.out.println("Default values of int : " + "     " + a);
        System.out.println("Default values of short : " + "   " + b);
        System.out.println("Default values of long : " + "    " + c);
        System.out.println("Default values of double : " + "  " + d);
        System.out.println("Default values of float : " + "   " + e);
        System.out.println("Default values of String : " + "   " + f);
        System.out.println("Default values of char : " + "     " + g);
        System.out.println("Default values of boolean : " + "   " + h);


    }
}
